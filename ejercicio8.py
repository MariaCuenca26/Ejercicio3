#ejercicio con operadores aritmeticos (modulo, exponente,division entera)
numero1 = int(input("Ingrese el primer numero numero: "))
numero2 = int(input("Ingrese el segundo numero: "))
#Procesos 
divisionEntera= numero1//numero2
exponente = numero1**numero2
modulo = numero1 % numero2
#salida
print("El resultado de la division entera es: " + str(divisionEntera))
print("El resultado del exponente es:" + str (exponente))
print("El resultado del modulo:" + str (modulo))

